package com.znz.tpip_backend.enums;

public enum EvaluationType {

    MIDTERM,        // Evaluation done halfway through the internship to check progress

    FINAL,          // Final evaluation at the end of the internship to determine readiness

    REASSESSMENT    // Re-evaluation after an extension to check if the intern has improved
}
// MIDTERM → checks how the intern is doing during the internship
// FINAL → determines if the intern passes and gets certificate
// REASSESSMENT → used after extension when intern tries again